class Reservation < ActiveRecord::Base

  # == Constants ============================================================

  # == Attributes ===========================================================

  # == Extensions ===========================================================

  # == Relationships ========================================================

  belongs_to :user

  # == Validations ==========================================================

  # == Scopes ===============================================================

  # == Callbacks ============================================================

  # == Class Methods ========================================================

  def self.for_period(from, to)
    where("reservations.reserved_at >= :from AND reservations.reserved_at <= :to", {
      from: from.to_s(:db),
      to: to.to_s(:db)
    })
  end

  # == Instance Methods =====================================================

end